import winsound
import time

# 小星星的音调和节奏列表
twinkle_twinkle = [
    (523, 500),  # C音符，持续500毫秒
    (523, 500),  # C音符，持续500毫秒
    (659, 500),  # E音符，持续500毫秒
    (659, 500),  # E音符，持续500毫秒
    (698, 500),  # F音符，持续500毫秒
    (698, 500),  # F音符，持续500毫秒
    (659, 1000), # E音符，持续1000毫秒
    (587, 500),  # D音符，持续500毫秒
    (587, 500),  # D音符，持续500毫秒
    (523, 500),  # C音符，持续500毫秒
    (523, 500),  # C音符，持续500毫秒
    (493, 500),  # B音符，持续500毫秒
    (493, 500),  # B音符，持续500毫秒
    (466, 1000), # A音符，持续1000毫秒
    (523, 500),  # C音符，持续500毫秒
    (523, 500),  # C音符，持续500毫秒
    (659, 500),  # E音符，持续500毫秒
    (659, 500),  # E音符，持续500毫秒
    (698, 500),  # F音符，持续500毫秒
    (698, 500),  # F音符，持续500毫秒
    (659, 1000), # E音符，持续1000毫秒
    (587, 500),  # D音符，持续500毫秒
    (587, 500),  # D音符，持续500毫秒
    (523, 500),  # C音符，持续500毫秒
    (523, 500),  # C音符，持续500毫秒
]

# 循环播放每个音符
for note in twinkle_twinkle:
    winsound.Beep(note[0], note[1])  # 使用winsound播放音符
    time.sleep(0.1)  # 等待一段时间以形成音符之间的间隔

# 最后暂停一秒钟
time.sleep(1)
